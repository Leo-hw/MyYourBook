package pack.controller;

public class CardInfoBean {

	private String card_ownerid, card_owner, card_comp, card_no, card_passwd;

	public String getCard_ownerid() {
		return card_ownerid;
	}

	public void setCard_ownerid(String card_ownerid) {
		this.card_ownerid = card_ownerid;
	}

	public String getCard_owner() {
		return card_owner;
	}

	public void setCard_owner(String card_owner) {
		this.card_owner = card_owner;
	}

	public String getCard_comp() {
		return card_comp;
	}

	public void setCard_comp(String card_comp) {
		this.card_comp = card_comp;
	}

	public String getCard_no() {
		return card_no;
	}

	public void setCard_no(String card_no) {
		this.card_no = card_no;
	}

	public String getCard_passwd() {
		return card_passwd;
	}

	public void setCard_passwd(String card_passwd) {
		this.card_passwd = card_passwd;
	}
	
	
	
}
